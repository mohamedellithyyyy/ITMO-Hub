import re
def solve(text):
    pattern = r'([А-Я][а-я]+(?:-[А-Я][а-я]+)?)\s+[А-Я]\.[А-Я]\.'
    
    # Find all matches
    surnames = re.findall(pattern, text)
    
    # Remove duplicates
    surnames = list(set(surnames))
    
    # Sort alphabetically
    surnames.sort()
    
    return surnames

